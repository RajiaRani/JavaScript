// Falsy Values
// A value is considered falsy if it translates to false when evaluated in a boolean context. 
// There are exactly six falsy values in JavaScript:
// 1. false - The boolean false
// 2.  0 - The number zero
// 3. -0 - The negative zero
// 4. " " - An empty string
// 5. null
// 6. undefined
// 7. NaN - Not a Number